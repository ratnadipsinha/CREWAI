const BLOCK_ICON: Record<BlockType, string> = {
  trigger: "zap",
  agent: "bot",
  task: "task",
  tool: "wrench",
  branch: "branch",
  human: "human",
  end: "flag",
};

const TOOL_ICON: Record<string, string> = {
  gmail: "mail",
  gmail_send: "send",
  outlook: "mail",
  jira: "kanban",
  hubspot: "database",
  netsuite: "database",
  ocr: "scan",
  mcp: "plug",
};

function Svg({ name, size = 24 }: { name: string; size?: number }) {
  return (
    <svg
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={2}
      strokeLinecap="round"
      strokeLinejoin="round"
      dangerouslySetInnerHTML={{ __html: P[name] ?? P.wrench }}
    />
  );
}

// Icon for a block; tool blocks use their connector-specific icon when known.
export function BlockIcon({
  type,
  toolKey,
  size = 24,
}: {
  type: BlockType;
  toolKey?: string;
  size?: number;
}) {
  const name =
    type === "tool" && toolKey && TOOL_ICON[toolKey]
      ? TOOL_ICON[toolKey]
      : BLOCK_ICON[type];
  return <Svg name={name} size={size} />;
}
